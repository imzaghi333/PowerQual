一、安装命令: yum install vim。

二、vim的三种模式: 
命令模式, 是默认的模式, 只能接收命令, 不能输入内容
shift+z+z: 保存退出
dd: 用来删除一行数据, ndd: 用来删除n行数据
u: 撤销
ctrl+r: 反撤销
yy: 用来复制一行数据, nyy用来复制n行数据
p: 用来粘贴
G: 定位到最后一行
gg: 定位到第一行, ngg: 定位到第n行
s: 定位到这一行的最后
0或^: 定位到这一行的行首
x: 删除光标右边的一个字符, nx: 删除光标右边的n个字符
X: 删除光标左边的一个字符, nX: 删除光标左边的n个字符
插入模式
按i键: 进入到插入模式
I: 在第一个非空字符前插入数据
a: 在光标的右侧插入数据
A: 在光标的结尾插入数据
s: 删除光标所在位置的字符, 并插入内容
S: 删除光标所在行的字符, 并插入内容
o: 在光标所在行的下一行插入内容
O: 在光标所在行的上一行插入内容
按esc键退出插入模式, 回到命令模式
底线命令模式
按:键(即shift+:), 进入底线命令模式
w: 保存修改
q: 退出, q!: 不保存, 强制退出
wq: 保存并退出, 等价于命令模式下的shift+z+z
eq: 放弃之前的修改
set nu: 显示 行号
n: 光标定位到n行
/内容: 查询指定内容, 按n键下一个匹配, 按N键上一个匹配
%s/原内容/替换内容: 默认只会替换每一行第一次匹配的内容。
%s/原内容/替换内容/g: 全部替换
m,ns/原内容/替换内容/g: 第m行到第n行的数据范围内做替换
n,$s/原内容/替换内容: 替换从第n行开始到最后一行

二、vim的配置文件: 
/etc/vimc: 对所有用户都能读取到这个配置。
~/.vimc: 只有当前用户才能读到该配置, 如若没有可以创建该文件。

